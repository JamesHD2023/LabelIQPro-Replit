// Translation helper function
export const t = (key, lang = 'en') => {
  const keys = key.split('.');
  let value = translations;
  
  for (const k of keys) {
    value = value[k];
    if (!value) break;
  }
  
  if (value && typeof value === 'object') {
    return value[lang] || value['en'] || key;
  }
  
  return key;
};

